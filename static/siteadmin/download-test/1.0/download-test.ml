    (fun fn ->
       if not (Sys.is_directory fn) then begin
         incr tested;
         try
           let md5_server = List.assoc (Filename.basename fn) lst in
           let md5_local = Digest.to_hex (Digest.file fn) in
             if md5_server = md5_local then begin
               Printf.printf "File %s OK.\n" fn
             end else begin
               incr mismatch;
               Printf.printf "Digest of file %s doesn't match (%s <> %s).\n" fn md5_server md5_local
             end
         with Not_found ->
           incr not_found;
           Printf.printf "File %s not found.\n" fn
       end)
    "test that files match the one on the forge";
